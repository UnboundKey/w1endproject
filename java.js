function neshide() {
    var x = document.getElementById('Neslist');
    if (x.style.display === 'none') {
        x.style.display = 'block';
    } else {
        x.style.display = 'none';
    }
}

function sneshide() {
    var x = document.getElementById('sneslist');
    if (x.style.display === 'none') {
        x.style.display = 'block';
    } else {
        x.style.display = 'none';
    }
}